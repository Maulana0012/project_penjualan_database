    private void ButtonIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonIActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel table1 = (DefaultTableModel) Table1.getModel();
            if(Edit1.getText().isEmpty() || Edit3.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Input tidak boleh kosong");
            }
            else{
                JOptionPane.showMessageDialog(null, (dbConnect.insertValueTransaksi(
                        Edit1.getText(), Combobox1.getSelectedItem().toString(),
                        Integer.parseInt(Edit3.getText()), Combobox2.getSelectedItem().toString())));
                table1.setRowCount(0);
                dbConnect.getForeignKey(Combobox1, "jual_produk", "Kode");
                dbConnect.getForeignKey(Combobox2, "konsumen", "Kode");   
                dbConnect.showtTable((DefaultTableModel)Table1.getModel(), "transaksi");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_ButtonIActionPerformed

